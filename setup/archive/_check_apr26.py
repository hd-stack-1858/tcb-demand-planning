# Build user map - fulfilled only (Shipped + Unshipped)
user_fulfilled = {}
user_qty = 0
for oid, asin, istatus, qty in user_raw:
    sku_id = resolve_amazon_sku(asin)
    key = f'AZ-{oid}-{sku_id}'
    if istatus != 'Cancelled':
        user_fulfilled[key] = {'qty': qty, 'status': istatus}
        user_qty += qty

