# DB Apr-26 fulfilled
db_rows = (db.table('orders').select('order_id,quantity,order_date')
             .in_('channel_id',[2,3]).eq('status','FULFILLED')
             .gte('order_date','2026-04-01').lte('order_date','2026-04-30').execute().data)
db_map = {r['order_id']: r for r in db_rows}
db_qty = sum(r['quantity'] for r in db_rows)
